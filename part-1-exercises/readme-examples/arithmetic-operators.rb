FIRST_ARGUMENT = 3
SECOND_ARGUMENT = 2

puts "Addition: #{FIRST_ARGUMENT + SECOND_ARGUMENT}" # addition; 5
puts "Subtraction: #{FIRST_ARGUMENT - SECOND_ARGUMENT}" # subtraction; 1
puts "multiplication: #{FIRST_ARGUMENT * SECOND_ARGUMENT}" # multiplication; 6
puts "Division: #{FIRST_ARGUMENT / SECOND_ARGUMENT}" # division; 1.5
puts "Exponentation: #{FIRST_ARGUMENT ** SECOND_ARGUMENT}" # exponentation; 9
puts "Modulus: #{FIRST_ARGUMENT % SECOND_ARGUMENT}" # modulus; 1